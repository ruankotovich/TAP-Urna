    private void jLbVotacaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLbVotacaoMouseClicked
        if (!PrivilegesDao.verifyDeadlock()) {
            if (jLbVotacao.isEnabled()) {
                int confirmation = JOptionPane.showConfirmDialog(null, "Deseja iniciar a votação?", "Votação", JOptionPane.OK_CANCEL_OPTION);
                if (confirmation == JOptionPane.OK_OPTION) {
                    boolean permissao = PrivilegesDao.verifyPrivileges(PasswordUtils.requestPassword("Inicio da votação", "Entre com a senha de administração : "));
                    if (permissao) {
                        startVote();
                    } else {
                        JOptionPane.showMessageDialog(null, "Permissão Negada.", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Opção desabilitada, insira suas informações.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "A votação foi encerrada por um administrador", "Erro", JOptionPane.ERROR_MESSAGE);
            int confirmation = JOptionPane.showConfirmDialog(null, "Deseja dar continuidade a votação?", "Votação", JOptionPane.OK_CANCEL_OPTION);
            if (confirmation == JOptionPane.OK_OPTION) {
                boolean permissao = PrivilegesDao.verifyPrivileges(PasswordUtils.requestPassword("Inicio da votação", "Entre com a senha de administração : "));
                if (permissao) {
                    PrivilegesDao.revokeDeadlock();
                    startVote();
                } else {
                    JOptionPane.showMessageDialog(null, "Permissão Negada.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jLbVotacaoMouseClicked

