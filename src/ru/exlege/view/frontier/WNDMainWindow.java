    private synchronized void verifyConnection() {
        new Thread(() -> {
            System.err.println("[EVENT] Windows gained focus.");
            while (this.isFocused()) {
                System.err.println("[EVENT] Verifying connection");
                if (ConnectionFactory.testPeriodicConnection()) {
                    jLbConnection.setIcon(new ImageIcon(this.getClass().getResource("/ru/exlege/view/gfx/connected.png")));
                    jLbConnection.setToolTipText("Connected");
                } else {
                    jLbConnection.setIcon(new ImageIcon(this.getClass().getResource("/ru/exlege/view/gfx/disconnected.png")));
                    jLbConnection.setToolTipText("Disconnected!");
                }
                try {
                    Thread.sleep(6000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(WNDMainWindow.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            System.err.println("[EVENT] Windows lost focus.");
        }).start();
    }

