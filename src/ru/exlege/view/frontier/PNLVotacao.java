    private void jLcandidatosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jLcandidatosValueChanged
        jButton1.setEnabled(true);
        jTimagem.setText("");
        jTimagem.setIcon(null);
        current = listModel.getElementAt(jLcandidatos.getSelectedIndex());
        jTnome.setText(current.getNome());
        if (!current.getNome().equals("VOTO EM BRANCO") && current.getPid() != 0) {
            jTpartido.setText(current.getPartido());
            jTcodigo.setText(String.valueOf(current.getPid()));
            jPfoto.setVisible(true);
        } else {
            jTpartido.setText("");
            jTcodigo.setText("");
            jPfoto.setVisible(false);
        }

        if (current.getFoto() != null) {
            jTimagem.setText("");
            try {
                jTimagem.setIcon(new ImageIcon(ImageIO.read(current.getFoto().getBinaryStream())));
            } catch (SQLException | IOException ex) {
                Logger.getLogger(PNLVotacao.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            jTimagem.setText("SEM FOTO");
        }

    }//GEN-LAST:event_jLcandidatosValueChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (current != null) {
            int confirmacao = JOptionPane.showConfirmDialog(null, "Deseja consumar seu voto?", "Voto", JOptionPane.INFORMATION_MESSAGE);
            if (confirmacao == JOptionPane.OK_OPTION) {
                if (VotoDao.commitVote(eleitor.getTitulo(), current.getPid(), current.getNome().equals("VOTO EM BRANCO"))) {
                    getCaller().addContent(new PNLEnding(getCaller()));
                } else {
                    JOptionPane.showMessageDialog(null, "Um erro ocorreu\nConsulte o administrador do sistema", "Erro", JOptionPane.ERROR_MESSAGE);
                    System.exit(0);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um candidato, voto nulo ou voto em branco", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


