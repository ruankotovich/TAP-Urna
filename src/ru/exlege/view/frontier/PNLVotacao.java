    public PNLVotacao(WNDVotacao calling, Eleitor eleitorCall) {
        super(calling);
        initComponents();
        jLcandidatos.setModel(new DefaultListModel<>());
        listModel = (DefaultListModel) jLcandidatos.getModel();

        generateWhite();
        generateNull();

        for (Candidato can : getCaller().getCandidatos()) {
            listModel.addElement(can);
        }

        this.eleitor = eleitorCall;
        String nomePaths[] = eleitorCall.getNome().split(" ");
        String newName = nomePaths.length > 2 ? nomePaths[0] + " " + nomePaths[1] : nomePaths[0];
        jTbemvindo.setText(jTbemvindo.getText().replace("<$>", newName));
        jTtitulo.setText(jTtitulo.getText().replace("<$>", String.valueOf(eleitor.getTitulo())));
        jTnascimento.setText(jTnascimento.getText().replace("<$>", eleitor.getNascimento()));
    }

    private void generateWhite() {
        Candidato branco = CandidatoDao.getWhite();
        if (branco != null) {
            Candidato existente = new Candidato(branco.getPid()) {
                @Override
                public String toString() {
                    return "VOTO EM BRANCO";
                }
            };
            existente.setNome("VOTO EM BRANCO");
            existente.setPartido("");
            listModel.addElement(existente);
        }
    }

    private void generateNull() {
        Candidato nulo = new Candidato(0) {
            @Override
            public String toString() {
                return "VOTO NULO";
            }
        };
        nulo.setNome("VOTO NULO");
        nulo.setPartido("");
        listModel.addElement(nulo);
    }

